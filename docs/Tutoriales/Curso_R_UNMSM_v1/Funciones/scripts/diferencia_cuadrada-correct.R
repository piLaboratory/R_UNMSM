# A continuación verás un ejemplo de una función llamada mi_suma(). Esta función
# recibe dos argumentos: 'a' y 'b'.
#
# mi_suma <- function(a, b = 1){
#     a + b
# }
#
# Si observas dentro de los paréntesis verás que he establecido b = 1.
# Esto significa que el argumento 'b' tiene el valor por defecto 1.
#
# Yo puedo usar la función mi_suma() sin proveer el valor de 'b':
# Por ejemplo, si mando llamar la función mi_suma(10), ésta regresará el valor
# 11.
#
# Pero si deseo proveer un valor para el argumento 'b':
# mi_suma(9, 4), ésta regresará el valor 13.
#
# Ahora escribirás una función llamada diferencia_cuadrada, la cual tomará dos
# argumentos: 'x' y 'y' y regresará la resta de cada uno de los elementos
# elevados al cuadrado. Y ahora supón que normalmente querrás que 'y' sea 2,
# entonces debes establecer el valor por defecto de 'y' a 2. Es necesario que
# 'x' sea el primer argumento y 'y' el segundo.
#
# Hint #1: Recuerda que para elevar un número a cierta potencia puedes usar el
# operador binario `^`.
#   Ej: 2 ^ 3 evalúa a 8.
#
# Recuerda establecer los valores por defecto apropiados. Cerciórate de guardar
# tus cambios e ingresar submit() en la línea de comandos cuando hayas terminado.

diferencia_cuadrada <- function(x, y=2) {
  x^2 - y^2
}
